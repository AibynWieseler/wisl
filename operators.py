#arithmetic operations
x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)

#comparison operators
x = 5
y = 3

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

#membership operators are used to test if a sequence is presented in an object
fruits = ["apple", "banana", "cherry"]

print("banana" in fruits) #to check if banana is in the fruits list

